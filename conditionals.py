# conditionals
a = 33
b = 26

# if statement
if a > b:
    print("a is greater than b")

# else statement
a = 33
b = 260

if a > b:
    print("a is greater than b")
else:
    print("a is not greater than b")

# elif statement
a = 200
b = 33

if b > a:
    print("b is greater than a")
elif a == b:
    print("a and b are equal values")
else:
    print("a is greater than b")