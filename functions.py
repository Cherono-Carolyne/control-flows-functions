# Functions: Block of code used repeatedly. It is executed when it is called.
def func_name():
    print("I am 24 years old")

func_name()

# Parsing arguments and parameters.
def my_func(fName):
    print(fName + " It is raining")

my_func("Caro,")

def my_func(fName, lName):
    print("Hello {} {}".format(fName, lName))

my_func("Carolyne", "Cherono")